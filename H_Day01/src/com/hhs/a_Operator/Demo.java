package com.hhs.a_Operator;

public class Demo {
    public static void main(String[] args) {
        System.out.println(3 + 7);//10
        System.out.println(3 - 7);//-4
        System.out.println(3 * 7);//21
        System.out.println(3 / 7);//0
        System.out.println(3.0 / 7);//0
        System.out.println(3 % 7);//3
        System.out.println(7 % 3);//1
    }
}
